public class SwitchDynamic {
    public static void main(String[] args) {


        switch (args[0]){
            case "0":
                System.out.println("monday");
                break;
            case "1":
                System.out.println("tuesday");
                break;
            case "2":
                System.out.println("wednesday");
                break;
            case "3":
                System.out.println("thrusday");
                break;
        }
        //System.out.println(args);
    }
}
