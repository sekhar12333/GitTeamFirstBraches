public class Switch {
    public static  void main(String[] args){
        int number = 12;

        switch (number){
            case 10:
                System.out.println("TEN");
                break;
            case 11:
                System.out.println("eleven");
                break;
            case 12:
                System.out.println("tweleve");
                break;
            case 13:
                System.out.println("thirteen");
                break;
                default:System.out.println("Not a Valid Number");
        }
    }
}
